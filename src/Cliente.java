//Definir o modelo de cliente dentro da aplicação :D

public class Cliente {

	//Características, atributo ou propriedades :O
	private String nome;
	private String cpf;
	
	//Getters and Setters
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	

	public void setCPF(String cpf) {
		this.cpf = cpf;	
	}
	
	public String getCPF() {
		return cpf;
	}
	
	
	
	
	
}
